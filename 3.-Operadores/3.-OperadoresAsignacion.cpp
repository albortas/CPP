#include<iostream>
using namespace std;

int main(){
    //Operador +=
    int a = 20;
    cout << "Valor de a: " << a << endl;
    a += 5;
    cout << "Valor de a: " << a << endl;
    //Operador -=
    a -= 3;
    cout << "Valor de a: " << a << endl;
    //Operador *=
    a *= 2;
    cout << "Valor de a: " << a << endl;
    //Operador /=
    float b = 9;
    b /= 2;
    cout << "Valor de b: " << b << endl;
    //Operador %=
    a = 9;
    a %= 2;
    cout << "Valor de a: " << a << endl;


    return 0;
}